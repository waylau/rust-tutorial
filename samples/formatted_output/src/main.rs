    // 第2行
    // 居中对齐
    print!("{:^3}", "x");
    // 左对齐
    println!("{:<2}", "x"); 

    // 第3行
    // 居中对齐
    println!("{:^5}", "x");

    // 第4行
    // 居中对齐
    print!("{:^3}", "x");
    // 左对齐
    println!("{:<2}", "x"); 

    // 第5行
    // 左对齐
    print!("{:<3}", "x");
    // 右对齐
    println!("{:>2}", "x"); 
